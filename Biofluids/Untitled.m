% Terminal velocity of falling objects.

m = [
    124*10^(-12)
    247*10^(-12);
    70/1000000;
    45.93/1000];

A = [
    7.068*10^(-9);
    6.361*10^(-9);
    1.963*10^(-5);
    1.429*10^(-3);
    ];

L = [
    30/1000000;
    0.09/1000;
    0.5/100;
    42.67/1000];

v = 14.88*10^(-6);
g = 9.81;
rho = 1.212;

Re =@(u) u.*L/v;
Cd =@(Re) 24./Re.*(1+0.173.*(Re).^0.657 )+0.413./(1+1.63.*(10)^(-4).*(Re).^(-1.09));
U =@(Cd)sqrt(2.*m*g./(rho.*A.*Cd));

fsolve(@(u) u - U(Cd(Re(u))),[10 10 10 10]',optimset('Display','off')')